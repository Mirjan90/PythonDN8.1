mood = input("Enter mood (Happy, Nervous, Sad, Relaxed, aything else?): ")

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Try to think something happy, no need to be sad :).")

elif mood == "relaxed":
    print("That is great! Just enjoy the day!")

else:
    print("I don't recognize this mood.")
